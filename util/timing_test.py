import pytest

import util.timing as t

def test_milliseconds_to_timestring():
    assert t.milliseconds_to_timestring(      678  ) ==  '00:00:00'
    assert t.milliseconds_to_timestring(      678.0) ==  '00:00:00'
    assert t.milliseconds_to_timestring(     5678  ) ==  '00:00:05'
    assert t.milliseconds_to_timestring(     5678.0) ==  '00:00:05'
    assert t.milliseconds_to_timestring(    45678  ) ==  '00:00:45'
    assert t.milliseconds_to_timestring(    45678.0) ==  '00:00:45'
    assert t.milliseconds_to_timestring(   345678  ) ==  '00:05:45'
    assert t.milliseconds_to_timestring(   345678.0) ==  '00:05:45'
    assert t.milliseconds_to_timestring(  2345678  ) ==  '00:39:05'
    assert t.milliseconds_to_timestring(  2345678.0) ==  '00:39:05'
    assert t.milliseconds_to_timestring( 12345678  ) ==  '03:25:45'
    assert t.milliseconds_to_timestring( 12345678.0) ==  '03:25:45'
    assert t.milliseconds_to_timestring( 98765432  ) ==  '27:26:05'
    assert t.milliseconds_to_timestring( 98765432.0) ==  '27:26:05'
    assert t.milliseconds_to_timestring(999999999  ) == '277:46:39'
    assert t.milliseconds_to_timestring(999999999.0) == '277:46:39'
    assert t.milliseconds_to_timestring(      678  , True) ==  '00:00:00.678'
    assert t.milliseconds_to_timestring(      678.0, True) ==  '00:00:00.678'
    assert t.milliseconds_to_timestring(     5678  , True) ==  '00:00:05.678'
    assert t.milliseconds_to_timestring(     5678.0, True) ==  '00:00:05.678'
    assert t.milliseconds_to_timestring(    45678  , True) ==  '00:00:45.678'
    assert t.milliseconds_to_timestring(    45678.0, True) ==  '00:00:45.678'
    assert t.milliseconds_to_timestring(   345678  , True) ==  '00:05:45.678'
    assert t.milliseconds_to_timestring(   345678.0, True) ==  '00:05:45.678'
    assert t.milliseconds_to_timestring(  2345678  , True) ==  '00:39:05.678'
    assert t.milliseconds_to_timestring(  2345678.0, True) ==  '00:39:05.678'
    assert t.milliseconds_to_timestring( 12345678  , True) ==  '03:25:45.678'
    assert t.milliseconds_to_timestring( 12345678.0, True) ==  '03:25:45.678'
    assert t.milliseconds_to_timestring( 98765432  , True) ==  '27:26:05.432'
    assert t.milliseconds_to_timestring( 98765432.0, True) ==  '27:26:05.432'
    assert t.milliseconds_to_timestring(999999999  , True) == '277:46:39.999'
    assert t.milliseconds_to_timestring(999999999.0, True) == '277:46:39.999'
